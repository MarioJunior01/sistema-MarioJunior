    private void mpj_jBtnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mpj_jBtnOKActionPerformed
        // TODO add your handling code here:
        int selectedRow = mpj_jTbProdutos.getSelectedRow();
        if (selectedRow != -1) {
            MpjTbProduto produtos = controllerProdutos.getBean(selectedRow);
            if (produtos != null) {
                jdlgProdutos.beanView(produtos);
            }
            this.setVisible(false);
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um produto primeiro", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_mpj_jBtnOKActionPerformed

