                Util.mensagem("Produto editado  com sucesso ");
                HistoricoTransacoes.salvar("UPDATE PRODUTO", "ID PRODUTO" + viewBeanProdutos().getMpjIdProduto() + "Nome Produto" + viewBeanProdutos().getMpjNomeProduto());

